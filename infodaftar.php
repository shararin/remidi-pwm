					<b><h4>Alih Jalur dan Pindahan</h4></b>
					<font size="3">
					<b> Alih Jalur</b>
					<p>
					Mahasiswa alih jalur adalah mahasiswa pindah jenjang dari D-3 STMIK AKAKOM (dalam) atau bukan STMIK AKAKOM (luar) ke S-1 STMIK AKAKOM, 
					dan yang bersangkutan telah dinyatakan lulus D3. 
					</p>
					<p>
					SYARAT
					
					<ol>
					    <li>Telah lulus D-3 dengan jurusan/program studi yang sesuai (dari kategori fakultas teknik / informatika)</li>
						<li>Calon peserta alih jalur akan diseleksi dengan pertimbangan kondisi calon, seperti: </li>
						    <ol>
						        <li>Kesesuaian jurusan/program studi
						        <li>Indeks prestasi kumulatif ≥ 2,50 (apabila IP < 2,50, sebelum mendaftar harus membuat surat permohonan untuk alih 
								    jalur ke Puket I dan disetujui)</li>
						        <li>Transkrip nilai</li>
							</ol>
						<li>Mata kuliah yang diselesaikan bagi mahasiswa alih jalur adalah mata kuliah tingkat sarjana, termasuk mata kuliah wajib pada jenjang sarjana pada tingkat bawahnya. 
						Sebagai bahan pertimbangan, beban mata kuliah yang harus diambil oleh setiap peserta dikaitkan dengan bobot mata kuliah D-3 dan tingkat kesetaraan mata kuliah </li>
						<li>Beban studi yang dapat diambil untuk pertama kali maksimal 21 SKS </li>
						<li>Pembayaran SPP Untuk Alih Jalur dari Luar disesuaikan dengan mahasiswa baru dengan Sumbangan Pengembangan Akademik (SPA) </li> 
						<li>Bagi mahasiswa alih jalur dari dalam, tidak dikenakan SPA dan besaran SPP Tetap dan variabel  </li>
					</ol>
					</p>
					<b>Pindahan</b>
					<p>
					Mahasiswa pindahan adalah mahasiswa dari luar STMIK AKAKOM atau dari STMIK AKAKOM yang pindah ke STMIK AKAKOM dan yang bersangkutan belum dinyatakan lulus.
					</p>
					<p>
					SYARAT
					<ol>
					    <li>    Berasal dari jurusan/program studi yang sesuai</li>
						<li>	Bersedia menerima hasil evaluasi nilai (anfulen) yang telah disesuaikan dengan kurikulum STMIK AKAKOM (Mata kuliah yang diakui
						        adalah mata kuliah yang materinya sama/setara dengan jenjang/jurusan baru dan proses anfulen ini dilakukan oleh Ketua Jurusan)</li>
						<li>	Untuk mahasiswa Pindahan dari dalam, SPP disesuaikan dengan mahasiswa baru dan tanpa Sumbangan Pengembangan Akademik</li>
						<li>	Untuk mahasiswa Pindahan dari Luar, SPP dan SPA disesuaikan dengan mahasiswa baru </li>
						<li>	Untuk mahasiswa pindah ke jenjang yang lebih tinggi (D-3 ke S-1) IP kumulatif minimal 2,50</li>
					</ol>
